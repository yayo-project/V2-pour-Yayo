// ?lang=en / ?lang=ar in any URL forces that language (and remembers it) —
// lets the dealer flyer QR open vendre.html directly in English or Arabic.
let YAYO_LANG = (function () {
  try {
    const p = new URLSearchParams(location.search).get("lang");
    if (p && I18N[p]) { localStorage.setItem("yayo_lang", p); return p; }
  } catch (e) { /* very old browser */ }
  return localStorage.getItem("yayo_lang") || "fr";
})();
if (!I18N[YAYO_LANG]) YAYO_LANG = "fr";

function t(key) {
  return (I18N[YAYO_LANG] && I18N[YAYO_LANG][key]) || I18N.fr[key] || key;
}

// Data values (fuel) are stored in French — translate for display only
const FUEL_KEYS = { "Essence": "fuel_petrol", "Diesel": "fuel_diesel", "Hybride": "fuel_hybrid", "Électrique": "fuel_ev" };
function tFuel(v) { return FUEL_KEYS[v] ? t(FUEL_KEYS[v]) : (v || ""); }

function applyI18n(root) {
  (root || document).querySelectorAll("[data-i18n]").forEach(el => { el.textContent = t(el.dataset.i18n); });
  (root || document).querySelectorAll("[data-i18n-html]").forEach(el => { el.innerHTML = t(el.dataset.i18nHtml); });
  (root || document).querySelectorAll("[data-i18n-ph]").forEach(el => { el.placeholder = t(el.dataset.i18nPh); });
  (root || document).querySelectorAll("[data-i18n-title]").forEach(el => { el.title = t(el.dataset.i18nTitle); });
}

function setLang(lang) {
  if (!I18N[lang]) return;
  YAYO_LANG = lang;
  localStorage.setItem("yayo_lang", lang);
  document.documentElement.lang = lang;
  document.documentElement.dir = lang === "ar" ? "rtl" : "ltr";
  document.querySelectorAll(".lang button").forEach(b => b.classList.toggle("on", b.dataset.lang === lang));
  applyI18n();
  if (typeof window.onLangChange === "function") window.onLangChange();
}

document.addEventListener("DOMContentLoaded", () => {
  // Globe + "Langue" label so everyone understands what FR/EN/AR is
  document.querySelectorAll(".lang").forEach(box => {
    if (box.querySelector(".lang-lbl")) return;
    const s = document.createElement("span");
    s.className = "lang-lbl";
    s.innerHTML = '<svg viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2" aria-hidden="true"><circle cx="12" cy="12" r="10"/><path d="M2 12h20M12 2a15.3 15.3 0 0 1 4 10 15.3 15.3 0 0 1-4 10 15.3 15.3 0 0 1-4-10 15.3 15.3 0 0 1 4-10z"/></svg><span data-i18n="lang_lbl">Langue</span>';
    box.insertBefore(s, box.firstChild);
  });
  document.querySelectorAll(".lang button").forEach(b => {
    if (!b.dataset.lang) b.dataset.lang = b.textContent.trim().toLowerCase();
    b.addEventListener("click", () => setLang(b.dataset.lang));
  });
  setLang(YAYO_LANG);
});
