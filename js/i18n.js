let YAYO_LANG = localStorage.getItem("yayo_lang") || "fr";
if (!I18N[YAYO_LANG]) YAYO_LANG = "fr";

function t(key) {
  return (I18N[YAYO_LANG] && I18N[YAYO_LANG][key]) || I18N.fr[key] || key;
}

// Data values (fuel) are stored in French — translate for display only
const FUEL_KEYS = { "Essence": "fuel_petrol", "Diesel": "fuel_diesel", "Hybride": "fuel_hybrid", "Électrique": "fuel_ev" };
function tFuel(v) { return FUEL_KEYS[v] ? t(FUEL_KEYS[v]) : (v || ""); }

function applyI18n(root) {
  (root || document).querySelectorAll("[data-i18n]").forEach(el => { el.textContent = t(el.dataset.i18n); });
  (root || document).querySelectorAll("[data-i18n-html]").forEach(el => { el.innerHTML = t(el.dataset.i18nHtml); });
  (root || document).querySelectorAll("[data-i18n-ph]").forEach(el => { el.placeholder = t(el.dataset.i18nPh); });
}

function setLang(lang) {
  if (!I18N[lang]) return;
  YAYO_LANG = lang;
  localStorage.setItem("yayo_lang", lang);
  document.documentElement.lang = lang;
  document.documentElement.dir = lang === "ar" ? "rtl" : "ltr";
  document.querySelectorAll(".lang button").forEach(b => b.classList.toggle("on", b.dataset.lang === lang));
  applyI18n();
  if (typeof window.onLangChange === "function") window.onLangChange();
}

document.addEventListener("DOMContentLoaded", () => {
  document.querySelectorAll(".lang button").forEach(b => {
    if (!b.dataset.lang) b.dataset.lang = b.textContent.trim().toLowerCase();
    b.addEventListener("click", () => setLang(b.dataset.lang));
  });
  setLang(YAYO_LANG);
});
